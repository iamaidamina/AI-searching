package backend;


public class Position {
    private Integer i;
    private Integer j;

    public Position(Integer i, Integer j) {
        this.i = i;
        this.j = j;
    }
    
    public Integer getI() {
        return i;
    }

    public void setI(Integer i) {
        this.i = i;
    }

    public Integer getJ() {
        return j;
    }

    public void setJ(Integer j) {
        this.j = j;
    }

 
    
}
